public class RucksackGroup {
    private String rucksackOne;
    private String rucksackTwo;
    private String rucksackThree;

    public RucksackGroup(String rucksackOne, String rucksackTwo, String rucksackThree) {
        this.rucksackOne = rucksackOne;
        this.rucksackTwo = rucksackTwo;
        this.rucksackThree = rucksackThree;
    }

    public String getRucksackOne() {
        return rucksackOne;
    }

    public void setRucksackOne(String rucksackOne) {
        this.rucksackOne = rucksackOne;
    }

    public String getRucksackTwo() {
        return rucksackTwo;
    }

    public void setRucksackTwo(String rucksackTwo) {
        this.rucksackTwo = rucksackTwo;
    }

    public String getRucksackThree() {
        return rucksackThree;
    }

    public void setRucksackThree(String rucksackThree) {
        this.rucksackThree = rucksackThree;
    }
}
